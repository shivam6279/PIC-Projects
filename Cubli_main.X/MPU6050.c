void GetFIFO(unsigned char *temp) {
	I2C_ReadRegisters(MPU6050_ADDR, 0x74, temp, 42);
}

unsigned int GetFIFOCount() {
	unsigned char temp[2];
	I2C_ReadRegisters(MPU6050_ADDR, 0x72, temp, 2);
	return (((unsigned short)temp[0]) << 8) | (unsigned short)temp[1];
}

void dmpDecode(unsigned char temp[42], float q[4], XYZ *gravity, float *pitch, float *roll, float *yaw) {
	q[0] = (signed short)(temp[0] << 8 | temp[1]);
	q[1] = (signed short)(temp[4] << 8 | temp[5]);
	q[2] = (signed short)(temp[8] << 8 | temp[9]);
	q[3] = (signed short)(temp[12] << 8 | temp[13]);

	q[0] /= 16384.0;
	q[1] /= 16384.0;
	q[2] /= 16384.0;
	q[3] /= 16384.0;

	gravity->x = q[1]*q[3] - q[0]*q[2];
	gravity->y = q[0]*q[1] + q[2]*q[3];
	gravity->z = (q[0]*q[0] - q[1]*q[1] - q[2]*q[2] + q[3]*q[3]) / 2.0;

	*pitch = atan2(2*q[1]*q[2] - 2*q[0]*q[3], 2*q[0]*q[0] - 2*q[1]*q[1] - 1) * 180.0 / M_PI;
	*roll = atan2(gravity->x, sqrt(gravity->y*gravity->y + gravity->z*gravity->z)) * 180.0 / M_PI;
	*yaw = atan2(gravity->y, gravity->z) * 180.0 / M_PI;

	if (gravity->z < 0) {
        if(*pitch > 0) {
            *pitch = 180.0 - *pitch; 
        } else { 
            *pitch = -180.0 - *pitch;
        }
    }
}

