void matrix_3x3_add(float a[][3], float b[][3], float c[][3]) {
	c[0][0] = a[0][0] + b[0][0];
	c[0][1] = a[0][1] + b[0][1];
	c[0][2] = a[0][2] + b[0][2];
	c[1][0] = a[1][0] + b[1][0];
	c[1][1] = a[1][1] + b[1][1];
	c[1][2] = a[1][2] + b[1][2];
	c[2][0] = a[2][0] + b[2][0];
	c[2][1] = a[2][1] + b[2][1];
	c[2][2] = a[2][2] + b[2][2];
}

void matrix_3x3_subtract(float a[][3], float b[][3], float c[][3]) {
	c[0][0] = a[0][0] - b[0][0];
	c[0][1] = a[0][1] - b[0][1];
	c[0][2] = a[0][2] - b[0][2];
	c[1][0] = a[1][0] - b[1][0];
	c[1][1] = a[1][1] - b[1][1];
	c[1][2] = a[1][2] - b[1][2];
	c[2][0] = a[2][0] - b[2][0];
	c[2][1] = a[2][1] - b[2][1];
	c[2][2] = a[2][2] - b[2][2];
}

void matrix_3x3_scale(float a[][3], float s, float b[][3]) {
	b[0][0] = s * a[0][0];
	b[0][1] = s * a[0][1];
	b[0][2] = s * a[0][2];
	b[1][0] = s * a[1][0];
	b[1][1] = s * a[1][1];
	b[1][2] = s * a[1][2];
	b[2][0] = s * a[2][0];
	b[2][1] = s * a[2][1];
	b[2][2] = s * a[2][2];
}

void matrix_3x3_elementwise_multiply(float a[][3], float b[][3], float c[][3]) {
	c[0][0] = a[0][0] * b[0][0];
	c[0][1] = a[0][1] * b[0][1];
	c[0][2] = a[0][2] * b[0][2];
	c[1][0] = a[1][0] * b[1][0];
	c[1][1] = a[1][1] * b[1][1];
	c[1][2] = a[1][2] * b[1][2];
	c[2][0] = a[2][0] * b[2][0];
	c[2][1] = a[2][1] * b[2][1];
	c[2][2] = a[2][2] * b[2][2];
}

void matrix_3x3_multiply(float a[][3], float b[][3], float c[][3]) {
	c[0][0] = a[0][0]*b[0][0] + a[0][1]*b[1][0] + a[0][2]*b[2][0];
	c[0][1] = a[0][0]*b[0][1] + a[0][1]*b[1][1] + a[0][2]*b[2][1];
	c[0][2] = a[0][0]*b[0][2] + a[0][1]*b[1][2] + a[0][2]*b[2][2];
	c[1][0] = a[1][0]*b[0][0] + a[1][1]*b[1][0] + a[1][2]*b[2][0];
	c[1][1] = a[1][0]*b[0][1] + a[1][1]*b[1][0] + a[1][2]*b[2][0];
	c[1][2] = a[1][0]*b[0][2] + a[1][1]*b[1][0] + a[1][2]*b[2][0];
	c[2][0] = a[2][0]*b[0][0] + a[2][1]*b[1][0] + a[2][2]*b[2][0];
	c[2][1] = a[2][0]*b[0][1] + a[2][1]*b[1][1] + a[2][2]*b[2][1];
	c[2][2] = a[2][0]*b[0][2] + a[2][1]*b[1][2] + a[2][2]*b[2][2];
}

float matrix_3x3_det(float a[][3]) {
	float det = 0;
	det += a[0][0] * (a[1][1]*a[2][2] - a[1][2]*a[2][1]);
	det -= a[0][1] * (a[1][0]*a[2][2] - a[1][2]*a[2][0]);
	det += a[0][2] * (a[1][0]*a[2][1] - a[1][1]*a[2][0]);

	return det;
}

void matrix_3x3_transpose(float a[][3], float b[][3]) {
	b[0][0] = a[0][0];
	b[0][1] = a[1][0];
	b[0][2] = a[2][0];
	b[1][0] = a[0][1];
	b[1][1] = a[1][1];
	b[1][2] = a[2][1];
	b[2][0] = a[0][2];
	b[2][1] = a[1][2];
	b[2][2] = a[2][2];
}

